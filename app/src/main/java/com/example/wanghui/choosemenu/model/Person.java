package com.example.wanghui.choosemenu.model;

public class Person {

    private  String name;
    private  String sex;
    private  Food food;

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public Food getFood() {
        return food;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public void setFood(Food food) {
        this.food = food;
    }
}
